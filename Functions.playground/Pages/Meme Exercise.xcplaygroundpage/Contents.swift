}

func watermelonsGrow() {
    print("Where the watermelons grow")
}

func backToMyHome() {
    print("Back to my home")
}

func dareNotGo() {
    print("I dare not go")
}

func butIfIDo() {
    print("But if I do")
}

func myMotherWillSay() {
    print("My mother will say")
}

func haveYouSeengoat() {
    print("Have you seen a goat that lives by the boat")
}

func VerseOne() {
    downByTheBay()
    downByTheBay()
    watermelonsGrow()
    watermelonsGrow()
